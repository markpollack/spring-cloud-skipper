package org.springframework.cloud.skipper.controller;

import org.junit.Before;
import org.junit.Test;
import org.junit.runner.RunWith;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.cloud.skipper.AbstractMockMvcTests;
import org.springframework.http.MediaType;
import org.springframework.test.context.junit4.SpringRunner;
import org.springframework.test.web.servlet.MockMvc;
import org.springframework.test.web.servlet.MvcResult;
import org.springframework.test.web.servlet.setup.MockMvcBuilders;
import org.springframework.web.context.WebApplicationContext;

import static org.assertj.core.api.Assertions.assertThat;
import static org.springframework.test.web.servlet.request.MockMvcRequestBuilders.get;
import static org.springframework.test.web.servlet.result.MockMvcResultHandlers.print;
import static org.springframework.test.web.servlet.result.MockMvcResultMatchers.status;

/**
 * @author Mark Pollack
 */
public class AboutInfoControllerTests extends AbstractMockMvcTests {

	@Test
	public void aboutTest() throws Exception {
		MvcResult result = mockMvc.perform(get("/about"))
				.andDo(print())
				.andExpect(status().isOk()).andReturn();
		String aboutResponse = result.getResponse().getContentAsString();
		assertThat(aboutResponse).isEqualTo("{\"version\":\"1.0.0\"}");
	}
}
